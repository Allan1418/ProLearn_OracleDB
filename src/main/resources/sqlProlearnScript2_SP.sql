/*CAPITULO_HIJO*/
CREATE TABLE PROLEARN.TESTI (
  PARA VARCHAR2(2)
);


CREATE OR REPLACE PROCEDURE PROLEARN.CH_FINDBYID_SP(
    P_ID_CAPITULO_HIJO IN NUMBER,
    P_CAPITULO_HIJO OUT PROLEARN.FIDE_CAPITULO_HIJO_TB%ROWTYPE
)IS
BEGIN
    SELECT * INTO P_CAPITULO_HIJO FROM PROLEARN.FIDE_CAPITULO_HIJO_TB 
    WHERE CAPITULO_HIJO_TB_ID_CAPITULO_PK = P_ID_CAPITULO_HIJO;
    
    INSERT INTO PROLEARN.TESTI (PARA) VALUES ('A');
    
EXCEPTION
    WHEN NO_DATA_FOUND THEN P_CAPITULO_HIJO := NULL;

END;






